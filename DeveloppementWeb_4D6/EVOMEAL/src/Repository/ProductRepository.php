<?php

namespace App\Repository;

use App\Entity\Product;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Product>
 *
 * @method Product|null find($id, $lockMode = null, $lockVersion = null)
 * @method Product|null findOneBy(array $criteria, array $orderBy = null)
 * @method Product[]    findAll()
 * @method Product[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ProductRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    public function save(Product $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Product $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findWithCriteria($categoryName, $searchField)
    {
        //Create a 'SQL' query that is for the product table
        $queryBuilder = $this->createQueryBuilder('p');

        if ($searchField != null) {
            //Adds conditions to the baseline for the search
            $queryBuilder->andWhere('p.name LIKE :searchFilter')
                ->orWhere('p.description LIKE :searchFilter')
                ->setParameter('searchFilter', '%' . $searchField . '%');
        }

        if ($categoryName != null) {
            //Adds conditions to the baseline for the category
            $queryBuilder->andWhere('p.categoryName = :categoryName')
                ->setParameter('categoryName', $categoryName);
        }

        //Get result of 'SQL' query
        return $queryBuilder->getQuery()->getResult();
    }

